public class MovingButton {
    private int xMov;
    private int yMov;

    public MovingButton(int xMov, int yMov){
        this.xMov = xMov;
        this.yMov = yMov;
    }

    public int getxMov() {
        return xMov;
    }

    public int getyMov() {
        return yMov;
    }
}
